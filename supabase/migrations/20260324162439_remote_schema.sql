CREATE OR REPLACE FUNCTION "public"."handle_new_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
BEGIN
  -- 1. Create the user profile (your existing logic)
  INSERT INTO public.profiles (id, username, avatar_url)
  VALUES (
    new.id, 
    -- This pulls the part of the email before the @ as a temporary username
    split_part(new.email, '@', 1), 
    '' -- Default empty string for avatar
  );

  -- 2. Create default shelves for the new user (new logic)
  INSERT INTO public.shelves (user_id, name, is_default)
  VALUES 
    (NEW.id, 'Reading', true),
    (NEW.id, 'Read', true),
    (NEW.id, 'Want to Read', true),
    (NEW.id, 'Favorites', true);

  RETURN new;
END;
$$;


