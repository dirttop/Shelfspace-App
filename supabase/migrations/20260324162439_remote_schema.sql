CREATE OR REPLACE FUNCTION "public"."check_email_exists"("check_email" "text") RETURNS boolean
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1
    FROM auth.users
    WHERE email = check_email
  );
END;
$$;


